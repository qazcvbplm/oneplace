package com.ops.entity;

import java.math.BigDecimal;

public class App {
    private Integer id;

    private BigDecimal js;

    private BigDecimal sf;

    private BigDecimal cc;

    private BigDecimal gm;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public BigDecimal getJs() {
        return js;
    }

    public void setJs(BigDecimal js) {
        this.js = js;
    }

    public BigDecimal getSf() {
        return sf;
    }

    public void setSf(BigDecimal sf) {
        this.sf = sf;
    }

    public BigDecimal getCc() {
        return cc;
    }

    public void setCc(BigDecimal cc) {
        this.cc = cc;
    }

    public BigDecimal getGm() {
        return gm;
    }

    public void setGm(BigDecimal gm) {
        this.gm = gm;
    }
}